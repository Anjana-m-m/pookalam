CENTER_X = 340
CENTER_Y = 350  # Match Kathakali's center

def circ(len, clr):
    penup()
    home()
    goto(CENTER_X, CENTER_Y - (len + 50))
    pendown()
    color(clr)
    begin_fill()
    circle(len)
    end_fill()

# Outer dark red circle
circ(240, "#3d0706")

# Shift turtle to center for triangle pattern
goto(CENTER_X, CENTER_Y - 50)

def tripatt(len, clr):
    color(clr)
    for i in range(36):
        begin_fill()
        forward(len / math.tan(math.pi / 3.6))
        left(130)
        forward(len / math.sin(math.pi / 3.6))
        left(100)
        forward(len / math.sin(math.pi / 3.6))
        left(130)
        forward(len / math.tan(math.pi / 3.6))
        end_fill()
        left(10)
    left(5)

# Smaller triangle layers
tripatt(240, "#cf342b")
tripatt(215, "#f5700a")
tripatt(195, "#fcd349")
tripatt(175, "#ffeeb5")

# Smaller inner green circle
circ(160, "#014504")


